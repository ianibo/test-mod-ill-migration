package com.k_int;

/**
 * A result object that holds an id and name
 */
public class ResultIdName {
    /** The id of the object being referenced */
    public String id;

    /** The name of the object being referenced */
    public String name;

    public ResultIdName(String id, String name) {
        this.id = id;
        this.name = name;
    }
}
